public class DoWhileLoopExample {
    public static void main(String[] args) {
        int count = 1;

        // Do-while loop to print numbers from 1 to 5
        do {
            System.out.println("Count: " + count);
            count++; // Increment the counter
        } while (count <= 5);

        System.out.println("Loop ended");
    }
}
